import random
class Point:

    def __init__(self, x, y):
        """
    Init methos that initializes the point with x and y
        :param x: X coordinate value
        :param y:  Y coordinate value

        """

        self.x = x
        self.y = y

a = Point(2, 3)

print(a.x)
print(a.y)

b = Point(7, 9)
print(b.x)
print(b.y)




